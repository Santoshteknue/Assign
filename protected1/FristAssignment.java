package protected1;

public class FristAssignment 
{
 public static void main(String a[])
   {
	 primitiveType();
	 finalType();
   }

 public static void primitiveType()
 {
	 int number=123;
	 String name="Surabhi Rajurkar";
	 System.out.println(number);
	 System.out.println(name);
 }
 public static void finalType()
 {
	 final int number=0073;
	 final String name="surabhi";
	 System.out.println(number);
	 System.out.println(name);
	 
 }
}
 